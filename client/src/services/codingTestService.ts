const API_URL = 'http://localhost:3001/api';

export const generateChallenge = async (techStack: string, difficulty: string) => {
  console.log(`ðŸŽ¯ Generating ${difficulty} ${techStack} challenge...`);
  
  try {
    // Try backend first
    const response = await fetch(`${API_URL}/coding/generate-challenge`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      credentials: 'include',
      body: JSON.stringify({
        techStack,
        difficulty,
        count: 1,
      }),
    });

    if (response.ok) {
      const data = await response.json();
      if (data.success && data.challenges && data.challenges.length > 0) {
        console.log('âœ… Backend challenge generated successfully');
        return data.challenges[0];
      }
    }
    
    throw new Error('Backend failed or returned invalid data');
  } catch (error) {
    console.warn('âŒ Backend failed, trying Gemini API:', error);
    
    // Fallback to Gemini API
    try {
      const challenge = await generateChallengeWithGemini(techStack, difficulty);
      console.log('âœ… Gemini challenge generated successfully');
      return challenge;
    } catch (geminiError) {
      console.warn('âŒ Gemini also failed:', geminiError);
      
      // Final fallback to predefined challenges
      console.log('ðŸ“š Using predefined fallback challenge');
      return getFallbackChallenge(techStack, difficulty);
    }
  }
};

