export const executeCode = async (code: string, language: string, testCases: any[]) => {
  console.log(`ðŸ¤– Executing ${language} code using Gemini AI`);
  
  try {
    // Import Gemini service dynamically to avoid circular imports
    const { geminiService } = await import('./geminiService');
    
    // Use Gemini to execute the code
    const result = await geminiService.executeCode(code, language, testCases);
    
    console.log('âœ… Code execution successful using Gemini AI');
    return result;
    
  } catch (error) {
    console.error('âŒ Gemini code execution failed:', error);
    
    // Fallback to backend execution if Gemini fails
    console.log('ðŸ”„ Falling back to backend execution...');
    return await executeCodeWithBackend(code, language, testCases);
  }
};

// Fallback function to use backend execution
const executeCodeWithBackend = async (code: string, language: string, testCases: any[]) => {
  const maxRetries = 3;
  const retryDelay = 1000; // 1 second

  for (let attempt = 1; attempt <= maxRetries; attempt++) {
    try {
      console.log(`ðŸ”„ Backend code execution attempt ${attempt}/${maxRetries}`);
      
      const response = await fetch(`${API_URL}/code/execute`, {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        credentials: 'include',
        body: JSON.stringify({
          code,
          language,
          testCases,
        }),
      });

      if (!response.ok) {
        const errorText = await response.text();
        console.error(`âŒ Server responded with ${response.status}: ${errorText}`);
        throw new Error(`Code execution failed: ${response.status} ${response.statusText}`);
      }

      const result = await response.json();
      console.log(`âœ… Backend code execution successful on attempt ${attempt}`);
      return result;

    } catch (error) {
      console.error(`âŒ Backend code execution attempt ${attempt} failed:`, error);
      
      // Don't retry for certain errors
      if (error instanceof Error) {
        if (error.message.includes('CORS') || 
            error.message.includes('400') || 
            error.message.includes('401') || 
            error.message.includes('403')) {
          throw error; // Don't retry client errors
        }
      }

      // If this was the last attempt, throw the error
      if (attempt === maxRetries) {
        if (error instanceof Error && error.message.includes('Failed to fetch')) {
          throw new Error('Failed to fetch - Unable to connect to the server. Please check if the backend is running and try again.');
        }
        throw error;
      }

      // Wait before retrying
      console.log(`â³ Waiting ${retryDelay}ms before retry...`);
      await new Promise(resolve => setTimeout(resolve, retryDelay));
    }
  }
};
